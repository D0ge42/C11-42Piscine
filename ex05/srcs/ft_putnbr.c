#include "../includes/ft.h"

void ft_putnbr(int nb)
{
        long    n;

        n = nb;
        if (n < 0)
        {
                ft_putchar('-');
                n *= -1;
        }
        if (nb > 9)
                ft_putnbr(nb / 10);
        ft_putchar(nb % 10 + '0');
}
